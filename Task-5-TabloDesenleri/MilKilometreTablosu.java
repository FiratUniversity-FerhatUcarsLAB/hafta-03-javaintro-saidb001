public class MilKilometreTablosu {
    public static void main(String[] args) {
    double katsayi = 1.609;

    System.out.println("Mil\t Kilometre");
    System.out.println("------------------");
    System.out.println("1\t " + (1 * katsayi));
    System.out.println("5\t " + (5 * katsayi));
    System.out.println("10\t " + (10 * katsayi));
    System.out.println("20\t " + (20 * katsayi));
    System.out.println("50\t " + (50 * katsayi));
}

}
